package binarysearch

// 已知一个长度为 n 的数组，预先按照升序排列，经由 1 到 n 次 旋转 后，得到输入数组。例如，原数组 nums = [0,1,2,4,5,6,7] 在变
// 化后可能得到：
//
// 若旋转 4 次，则可以得到 [4,5,6,7,0,1,2]
// 若旋转 7 次，则可以得到 [0,1,2,4,5,6,7]
//
// 注意，数组 [a[0], a[1], a[2], ..., a[n-1]] 旋转一次 的结果为数组 [a[n-1], a[0], a[1], a[2],
// ..., a[n-2]] 。
//
// 给你一个元素值 互不相同 的数组 nums ，它原来是一个升序排列的数组，并按上述情形进行了多次旋转。请你找出并返回数组中的 最小元素 。
//
// 你必须设计一个时间复杂度为 O(log n) 的算法解决此问题。
//
// 示例 1：
//
// 输入：nums = [3,4,5,1,2]
// 输出：1
// 解释：原数组为 [1,2,3,4,5] ，旋转 3 次得到输入数组。
//
// 示例 2：
//
// 输入：nums = [4,5,6,7,0,1,2]
// 输出：0
// 解释：原数组为 [0,1,2,4,5,6,7] ，旋转 4 次得到输入数组。
//
// 示例 3：
//
// 输入：nums = [11,13,15,17]
// 输出：11
// 解释：原数组为 [11,13,15,17] ，旋转 4 次得到输入数组。
//
// 提示：
//
// n == nums.length
// 1 <= n <= 5000
// -5000 <= nums[i] <= 5000
// nums 中的所有整数 互不相同
// nums 原来是一个升序排序的数组，并进行了 1 至 n 次旋转

/*
思路：
不断缩小区间，并同步记录全局最小值，
当区间是一段完全递增的有序数组时（nums[low] < nums[high]），最小值自然是 nums[low]，此时更新全局最小值并退出循环即可。
为什么不会漏掉最小值呢？
*/

/*
思路：
旋转n次，即为将数组的后n个元素拼接到最前端。
所以数组整体分为两部分，每部分保持升序且元素各不相同。
由题目要求时间复杂度为 O(log n)，可以优先考虑二分查找：

  - 若当前元素处于左半部分，即nums[low] <= nums[mid]，说明mid指向整体元素较大的数组。
    因为原数组是升序的，旋转操作会将靠后部分较大的元素序列放置到靠前部分。
    所以此时应往右边缩小搜索范围。

    因为low只会在mid指向整体元素较大的数组时，改变为low + 1，
    所以下一轮处理开始时，若nums[low] < nums[high]，此时的搜索范围已经整体有序了，
    说明low已经踏入了整体元素较小的数组，直接取最左的元素即可。

- 反之应往左边缩小搜索范围，往两部分数组的拼接点逼近。
*/
func findMin(nums []int) int {
	res := nums[0]
	mid, low, high := 0, 0, len(nums)-1
	for low <= high {
		if nums[low] < nums[high] {
			res = min(res, nums[low])
			break
		}
		mid = low + (high-low)/2
		res = min(res, nums[mid])
		if nums[low] <= nums[mid] {
			low = mid + 1
		} else {
			high = mid - 1
		}
	}
	return res
}

/*
# 一、普通二分查找（找某个数：比如 5）
数组：`[1,3,5,7,9]`

循环条件：low ≤ high
因为：每一个位置都可能是答案，必须全部检查一遍

过程像这样：
```
[1,3,5,7,9]

	↑       ↑

low    high

→ 算mid
→ 不是目标，就扔掉一半
→ 继续……

最后变成：
[5]

	↑

low=high
→ 检查一下，是答案，返回
```

为什么要用 ≤？
因为最后这一个数也可能是你要找的数，必须进循环再判断一次。
不用 ≤ 就会漏掉它。

# 二、旋转数组找最小值（找“最后剩下的那个”）
数组：`[4,5,6,7,0,1,2]`

循环条件：low < high
因为：
我们不是找某个数，而是不断缩小范围，直到只剩一个数
那个数天然就是最小值

过程像这样：
```
[4,5,6,7,0,1,2]

	↑           ↑

low        high

→ 比较 mid 和 right
→ 扔掉不可能的一半
→ 区间越来越小

[0,1,2]

	↑   ↑

low high

→ 继续缩

[0,1]

	↑ ↑

low high

→ 还剩两个，继续
```

当 low == high 时：
```
[0]

	↑

low=high
```
已经只剩一个，不用再比了，它就是最小。

---

# 三、灵魂一句话总结（记这个就够）

## 普通二分（找某个数）
- 目标：在循环里找到就返回
- 所以：`low <= high`（最后一个也要检查）

## 找最小值（缩到只剩一个）
- 目标：把区间缩到只剩一个数
- 所以：`low < high`（只剩一个就停，不用再动）

# 四、最形象的比喻

### 普通二分
像是：
在人群里找一个叫“5”的人
每个人都要问一遍，直到找到。

### 找最小值
像是：
一群人打架，每次干掉一半弱的
最后活下来的那个，就是最强（最小）

不用问他是谁，他站在那，他就是答案。
*/
func findMinII(nums []int) int {
	low, high, mid := 0, len(nums)-1, 0
	for low < high { // 不断聚焦于有序且较小的一边，直到区间只剩一个元素，该元素即为结果
		mid = low + (high-low)/2
		if nums[mid] > nums[high] { // 说明落在被旋转的子数组，即较大的部分，结果肯定不会在这里，跳过不看
			low = mid + 1
		} else if nums[mid] <= nums[high] { // 说明 mid 所在的子数组是有序的，此时需要向子数组左侧收缩寻找最小值
			high = mid // high = mid 而不是 mid - 1，是因为 mid 本身可能就是最小值
		}
	}
	return nums[low] // 等价于 nums[high]
}
